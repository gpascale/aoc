def read_data(data):
  grid = []
  for line in data.splitlines():
    grid.append(list(line))
  return grid

def pprint(grid):
  for line in grid:
    print("".join(line))
  print()

def find_empty_rows(grid):
  empty = []
  for i, row in enumerate(grid):
    if all(c == '.' for c in row):
      empty.append(i)
  return empty

def expand_empty_rows(grid, empty):
  M = len(grid[0])
  offset = 0
  for i in empty:
    grid.insert(i + offset, ['.'] * M)
    offset += 1
  return grid

def expand_grid(grid):
  empty = find_empty_rows(grid)
  grid = expand_empty_rows(grid, empty)
  grid = list(zip(*grid))
  empty = find_empty_rows(grid)
  grid = expand_empty_rows(grid, empty)
  return list(zip(*grid))

def get_coords(grid):
  coords = []
  for i, row in enumerate(grid):
    for j, c in enumerate(row):
      if c == '#':
        coords.append((i, j))
  return coords

def distance(a, b):
  return abs(a[0] - b[0]) + abs(a[1] - b[1])

