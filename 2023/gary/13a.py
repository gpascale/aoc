def read_data(data):
  pats = data.split("\n\n")
  for i, pat in enumerate(pats):
    lines = pat.split("\n")
    pat = [ tuple(line) for line in lines ]
    pats[i] = pat
  return tuple(pats)

def pprint(pat):
  for line in pat:
    print("".join(line))

def find_reflection(pat):
  N = len(pat)
  for i in range(N-1):
    if pat[i] == pat[i+1]:
      found = True
      M = min(i, N-i-2)
      for j in range(1, M+1):
        if pat[i-j] != pat[i+j+1]:
          found = False
          break
      if found:
        return i
  return -1

